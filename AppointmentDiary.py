# Define the AppointmentDiary class
class AppointmentDiary:
    """Manages appointments for a user."""

    def __init__(self):
        self.appointments = []

    def add_appointment(self, appointment):
        """Adds an appointment to the diary."""
        self.appointments.append(appointment)


        
